package _02_Estruturas_Condicionais;

public class _01_Expressoes_comparativas {

	public static void main(String[] args) {
		// Expressões comparativas
		
				// OPERADOR                SIGNIFICADO
				
				//    >                       Maior
				//	  <                       Menor
				//	  >=                  Maior ou igual
				//	  <=                  Menor ou igual
				//	  ==					  Igual
				//	  !=                    Diferente

// *****************************************************************************
				
		//EXEMPLO [ x_ec = variavel x + expressão comparativa ]
				
				// Supondo que x_ec = 5 Então responda: x_ec > 0 | x_ec == 3 | 10 <= 30 | x_ec != 2
		
		int x_ec = 5;
		
		System.out.println(x_ec > 0);
		System.out.println(x_ec == 3);
		System.out.println(10 <= 30);
		System.out.println(x_ec != 2);
	}

}
